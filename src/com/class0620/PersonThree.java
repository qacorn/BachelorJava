package com.class0620;

public class PersonThree {

    private int age = 18;
    private String  name = "haujina";

    public PersonThree(int age, String name) {
        this.age = age;
        this.name = name;
    }

    public PersonThree() {
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
